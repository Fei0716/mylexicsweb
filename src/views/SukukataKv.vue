function initButtons(){
  let sukukataPage = sukukata[currentPage - 1];
  let sukukataArr = sukukataPage.sukukata_arr;
  for(let i= 0; i < sukukataArr.length;i++){
    let btnSpriteSheet = new createjs.SpriteSheet({
      images: [
        queue.getResult(`btn_${sukukataArr[i].sukukata}`), // Normal state
      ],
      frames: { width: sukukataArr[i].width ? sukukataArr[i].width : 285 , height: sukukataArr[i].height ? sukukataArr[i].height : 209}, // Adjust size as needed
      animations: {
        normal: 0,
      }
    });
    // Create the Sprite
    let btn = new createjs.Sprite(btnSpriteSheet, "normal");
    btn.mouseEnabled = true;
    btn.mouseChildren = true;
    btn.cursor = "pointer";
    btn.y = canvas.height * sukukataArr[i].y;
    btn.x =  canvas.width  * sukukataArr[i].x;
    btn.scale = isMobile.value ? .35 * dpr: .35 * dpr; // Apply scaling
    btn.on("click", () => {
      //if there is animation playing,stop the playing animation
      if(playingAnimation){
        playingAnimation.gotoAndPlay("stop");
        stage.removeChild(playingAnimation);
        playingAnimation = null;
      }
      playSound(`sound_${sukukataArr[i].sukukata}`);
      let animationSpriteSheet = new createjs.SpriteSheet({
        images: [queue.getResult(`animation_${sukukataArr[i].sukukata}`)],
        framerate: 30,
        frames: sukukataArr[i].frames,
        animations: {
          run: [0,sukukataArr[i].frames.length - 1, "end"],
          end: [sukukataArr[i].frames.length - 1,sukukataArr[i].frames.length - 1],
          stop: [0]
        }
      });
      let animation= new createjs.Sprite(animationSpriteSheet);
      animation.x = canvas.width * (isMobile.value ? .7 :.63);
      animation.y = canvas.height * (isMobile.value ? .47 :.45);
      animation.scale = (sukukataArr[i].scale ?? (isMobile.value ? 1.2 :1.4)) * dpr;
      stage.addChild(animation);
      animation.gotoAndPlay("run");
      playingAnimation = animation;

    });
    stage.addChild(btn);
    displayedButtons.push(btn);
  }
}


function navigateToAnotherPage(direction){
  /*
  * to handle page navigation when clicking on next or previous button
  * */
  if(playingAnimation){
    playingAnimation.gotoAndPlay("stop");
    stage.removeChild(playingAnimation);
    playingAnimation = null;
  }
  if(direction === "next"){
    if(currentPage >= 1 && currentPage < sukukata.length){
      hideDisplayButtons();
      addPreviousButton();
      currentPage++;
      initButtons();
    }
    else if(currentPage === sukukata.length){
      navigateToAnotherSubmenu();
    }
  }else if(direction === "previous"){
    if(currentPage > 1 && currentPage <= sukukata.length){
      hideDisplayButtons();
      currentPage--;
      initButtons();
    }
    else if(currentPage === 1){
      //hide the previous button
      hidePreviousButton();
    }
  }
}
function hideDisplayButtons(){
  /*
  * remove any sukukata buttons from the previous run
  * */
  displayedButtons.forEach((btn) => {
    stage.removeChild(btn);
  });

}
